// This program prints a triangle consisting of numbers.
public class triangleNum {
	
	public static void main(String[] args) {
		
		for (int line = 1; line <= 7; line++) {
			for (int i = 1; i <= (line); i++) 
				System.out.print(line);
				System.out.println();
		}
	}
}
